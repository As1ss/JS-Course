// AND, OR NOT

// &&

console.log(true && true,"operador AND, true && true");
console.log(false && true,"operador AND, false && true");


// ||
console.log(true || true,"operador OR, true || true");
console.log(false || true,"operador OR, false || true");
console.log(false || false,"operador OR, false || false");

// NOT

console.log(!true,"operador NOT, !true");
console.log(!false,"operador NOT, !false");